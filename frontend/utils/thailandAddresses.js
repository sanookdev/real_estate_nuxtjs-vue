// Helper function to get all provinces
export const getProvinces = () => {
    return Object.keys(thailandAddresses);
};

// Helper function to get districts by province
export const getDistricts = (province) => {
    if (!province || !thailandAddresses[province]) return [];
    return Object.keys(thailandAddresses[province].districts);
};

// Helper function to get subdistricts by province and district
export const getSubdistricts = (province, district) => {
    if (!province || !district || !thailandAddresses[province] || !thailandAddresses[province].districts[district]) return [];
    return Object.keys(thailandAddresses[province].districts[district].subdistricts);
};

// Helper function to get postal code
export const getPostalCode = (province, district, subdistrict) => {
    if (!province || !district || !subdistrict) return '';
    try {
        return thailandAddresses[province].districts[district].subdistricts[subdistrict] || '';
    } catch {
        return '';
    }
};
